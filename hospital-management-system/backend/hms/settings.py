# ---------------------------------------------------------------------------
# Database
#
# The thesis specifies MySQL for structured, secure storage of hospital
# data. Set DB_ENGINE=mysql and the DB_* variables (see .env.example) to use
# MySQL. Without those variables the project falls back to SQLite so the
# system can be run and evaluated without a MySQL server installed.
# ---------------------------------------------------------------------------
if config("DB_ENGINE", default="sqlite") == "mysql":
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.mysql",
            "NAME": config("DB_NAME", default="hms_db"),
            "USER": config("DB_USER", default="root"),
            "PASSWORD": config("DB_PASSWORD", default=""),
            "HOST": config("DB_HOST", default="localhost"),
            "PORT": config("DB_PORT", default="3306"),
            "OPTIONS": {"charset": "utf8mb4"},
        }
    }
else:
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }
    }

